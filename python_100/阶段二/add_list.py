# 两个数组相加：["10.202.101.42","10.202.101.43"] [":8080",":8081"] => ["10.202.101.42:8080","10.202.101.43:8081"]
def a():
    a = ["10.202.101.42","10.202.101.43"]
    b = [":8080",":8081"]
    c = []
    if len(a) == len(b):
        for i in range(0,len(a)):
            c.append(a[i]+b[i])

    print(c)



